package uk.ac.cam.mr2083.sect1;


public final class MathUtil {
    public static int max(int a, int b) {
        return a > b ? a : b; // return a if a is greater than b, else return b
    }

    public static double max(double a, double b) {
        return a > b ? a : b; // return a if a is greater than b, else return b
    }
}
